    public function AuthVerification(Request $request)
    {
        $auth = auth('sanctum')->user();
        $email = $auth->email;

        if (!$auth) {
            return response()->json(['message' => 'Anda belum terdaftar']);
        }

        $validator = Validator::make($request->all(), [
            'otp' => 'required|string|max:6',
        ]);

        if ($validator->fails()) {
            return response()->json($validator->errors());
        }

        $user = User::where('email', $email)->first();

        if ($user && $user->otp_code == $request->input('otp')) {
            if ($user->otp_expired && now()->lt($user->otp_expired)) {
                $user->email_verified_at = now();
                $user->save();

                return response()->json(['message' => 'Email berhasil di verifikasi silahkan kembali ke halaman login']);
            } else {
                return response()->json(['message' => 'Kode OTP sudah kedaluwarsa']);
            }
        } else {
            return response()->json(['message' => 'Kode OTP tidak valid']);
        }
    }
